package eldar.com;

public class PrimitiveTypes2 {


    public static void main(String[] args) {
        // we have 4 types of integers - whole numbers
        byte a = 99; // 8 bit
        short b = 9999; // 16 bit
        int c = 999999999; // 32 bit
        long d = 999999999999999999L; // 64 bit
        // we have 2 types of floats - decimal numbers
        float e = 5.45F; // 32 bit
        double f = 5.32; // 64 bit - more accurate
        System.out.println(e);
        System.out.println(f);
    }


}
