package eldar.com;

public class PrimitiveTypes {
    public static void main(String[] args) {
        // primitive types
        // numerals
        int num1 = 15;
        System.out.println(num1);

        double num2 = 5.32;
        System.out.println(num2);

        // characters
        char c1 = 'A';
        char c2 = 'א';
        char c3 = '!';

        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);

        // logical
        boolean b1 = true;
        boolean b2 = false;
        boolean b3 = 10 > 5;
        System.out.println(b3);
    }

}
