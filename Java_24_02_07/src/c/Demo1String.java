package c;

public class Demo1String {

    public static void main(String[] args) {
        // String is an object for text
        String txt = "Hello Java String";
        System.out.println(txt);

        // more examples of text data:
        String userName = "Eldar";
        String userAddress = "Haezel 31, Tel Aviv, Israel";
        System.out.println("user name: " + userName);
        System.out.println("user address: " + userAddress);
    }
}
