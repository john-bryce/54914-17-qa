package c.arrays;

public class Demo1 {
    public static void main(String[] args) {
        // create an array of 5 int elements
        int[] arr = new int[5];
        // insert elements to the array
        arr[0] = 9;
        arr[1] = 3;
        arr[2] = 12;
        arr[3] = 2;
        arr[4] = 100;
        // print elements
        System.out.println(arr[0]); // 9
        System.out.println(arr[1]); // 3

    }
}
